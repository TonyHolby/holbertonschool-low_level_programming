#include "main.h"

/**
 * _isalpha - Entry point
 *
 * Description: a function that checks for alphabetic character
 * with c as parameter
 *
 * Return: 1 if c is an alpha character and 0 if c is not an alpha character
 */

int _isalpha(int c)
{
	if (_isalpha(c))
		return (1);

	else
		return (0);
}
